class WeightedTripletLoss(nn.Module):
    """Related Weighted Triplet Loss theory can be found in paper
    'Attention Network Robustification for Person ReID'."""

    def __init__(self, margin=None):
        super(WeightedTripletLoss, self).__init__()
        self.margin = margin
        if margin is not None:
            self.ranking_loss = nn.MarginRankingLoss(margin=margin)
        else:
            self.ranking_loss = nn.SoftMarginLoss()

    def forward(self, inputs, targets, normalize_feature=False):
        if normalize_feature:
            inputs = normalize(inputs, axis=-1)

        dist_mat = euclidean_dist(inputs, inputs)
        dist_ap, dist_an, final_wp, final_wn = soft_example_mining(dist_mat, targets)
        y = final_wn.new().resize_as_(final_wn).fill_(1)
        if self.margin is not None:
            loss = self.ranking_loss(dist_an, dist_ap, y)
        else:
            loss = self.ranking_loss(final_wn - final_wp, y)
        return loss
